/*
relational operator
less than : <
greater than : >
less than or equal : <=
greater than or equal :>=
not equal : != 
both equal : == */
#include<stdio.h>
int main()
{
    int a=6,b=4;
    printf("a==b = %d\n",a==b);
    printf("a<b = %d\n",a<b);
    printf("a>b = %d\n",a>b);
    printf("a!=b = %d\n",a!=b);
}