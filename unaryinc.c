
//preincrement & postincrement operator
#include<stdio.h>
int main()
{
    int i=10,j=10,x;
    i = ++i;//i=i+1
    printf("i = %d\n",i);
    x = j++;
    printf("x = %d\n",x);
    printf("j = %d\n",j);
}

